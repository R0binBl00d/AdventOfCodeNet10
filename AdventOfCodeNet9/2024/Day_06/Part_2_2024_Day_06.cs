    private bool PathToRightLeadsToOldPath(Point currentLocation, int orientation, ref Dictionary<(int x, int y), List<int>> vectorField, ref char[,] field)
    {
      bool pathFound = false;
      switch (orientation)
      {
        case 0: // Top
          currentLocation.Y--;
          break;
        case 1: // Right
          currentLocation.X++;
          break;
        case 2: // Bottom
          currentLocation.Y++;
          break;
        case 3: // Left
          currentLocation.X--;
          break;
      }

      while
      (
        currentLocation.X.InRange(0, field.GetLength(0), IncludeBounds.Lower) &&
        currentLocation.Y.InRange(0, field.GetLength(1), IncludeBounds.Lower) &&
        field[currentLocation.X, currentLocation.Y] != '#'
      )
      {
        // check for correct Vector
        if (vectorField[(currentLocation.X, currentLocation.Y)].Contains(orientation))
        {
          pathFound = true;
          break;
        }
        else
        {
          switch (orientation)
          {
            case 0: // Top
              currentLocation.Y--;
              break;
            case 1: // Right
              currentLocation.X++;
              break;
            case 2: // Bottom
              currentLocation.Y++;
              break;
            case 3: // Left
              currentLocation.X--;
              break;
          }
        }
      }

      return pathFound;
    }

    private bool NextLocationIsInside(Point currentLocation)
    {
      if (
        currentLocation.X < 0 ||
        currentLocation.Y < 0 ||
        currentLocation.X == Lines[0].Length ||
        currentLocation.Y == Lines.Count
      )
      {
        return false;
      }
      return true;
    }
